// translations.js 파일 업데이트
let content = fs.readFileSync('src/utils/translations.js', 'utf8');

// ko 블록 업데이트
Object.entries(koTranslations).forEach(([key, val]) => {
    // 키=키값인 잘못된 항목을 올바른 한국어로 교체
    const badPattern = new RegExp(`"${key}":\\s*"${key.replace(/[.*+?^${}()|[\]\\]/g, '\\$&')}"`, 'g');
    const replacement = `"${key}": "${val}"`;
    content = content.replace(badPattern, replacement);
});

// en 블록 업데이트
Object.entries(enTranslations).forEach(([key, val]) => {
    const badPattern = new RegExp(`"${key}":\\s*"${key.replace(/[.*+?^${}()|[\]\\]/g, '\\$&')}"`, 'g');
    const replacement = `"${key}": "${val}"`;
    content = content.replace(badPattern, replacement);
});

fs.writeFileSync('src/utils/translations.js', content);
console.log('✅ Translations corrected');

